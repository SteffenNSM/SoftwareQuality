package Main;


 //Main class that serves as the entry point for the program.
 //This class demonstrates basic output and iteration in Java.

public class Main {
    //The main method that gets executed when the program runs.

     public static void main(String[] args) {
        // Print a welcome message to the console
        System.out.printf("Hello and welcome!");

        // Loop to demonstrate simple iteration and output
        for (int i = 1; i <= 5; i++) {
            // Print the current value of i
            System.out.println("i = " + i);
        }
    }
}